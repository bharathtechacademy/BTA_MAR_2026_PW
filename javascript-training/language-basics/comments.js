//Comments in Javascript

//Comments are used to explain code and make it more readable. 
//They are ignored by the JavaScript engine and do not affect the execution of the code.


//there are two types of comments in JavaScript:

//1. Single-line comments: These comments start with two forward slashes (//) and continue until the end of the line.
// Example:
console.log("This is a single-line comment"); // This is a comment

//short cut for single line comment is ctrl + / (windows) or cmd + / (mac)

//2. Multi-line comments: These comments start with a forward slash followed by an asterisk (/*) and end with an asterisk followed by a forward slash (*/). They can span multiple lines.
/* This is a multi-line comment
   It can span multiple lines
   and is useful for longer explanations or documentation. */
console.log("This is a multi-line comment");

//short cut for multi line comment is shift + alt + A (windows) or shift + option + A (mac)

/* console.log meaning , 
it's a print statement that outputs the specified 
message to the console.
*/